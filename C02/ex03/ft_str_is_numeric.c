int	ft_str_is_numeric(char *str)
{
	int	control;
	int	i;

	control = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
		{
			control = 0;
		}
		i++;
	}
	return (control);
}
