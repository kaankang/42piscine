int	ft_str_is_printable(char *str)
{
	int	i;
	int	control;

	i = 0;
	control = 1;
	while (str[i] != '\0')
	{
		if (str[i] <= 31 || str[i] == 127)
		{
			control = 0;
		}
		i++;
	}
	return (control);
}
