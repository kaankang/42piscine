int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	control;

	i = 0;
	control = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			control = 0;
		}
		i++;
	}
	return (control);
}
